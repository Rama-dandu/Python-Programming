#in and not in are the two membership operators which are used to check whether the specific character, number, substring present in the series of string or list etc.,
str = 'Jenny'
print('e' in str) # True
print('e' not in str) # False
print('enn' in str) #True
print('eny' in str) #False

#list
l = [1,10,-1,0,17]
print(1 in l) #True
print(20 in l) #False